package com.onpaging.bean;

public class Site {
	private int num;
	private String name;
	private String addre;
	private String kindx;
	private String etc;
	private String orderb;
	private String addrem;
	
	private String id;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddre() {
		return addre;
	}
	public void setAddre(String addre) {
		this.addre = addre;
	}
	public String getKindx() {
		return kindx;
	}
	public void setKindx(String kindx) {
		this.kindx = kindx;
	}
	public String getEtc() {
		return etc;
	}
	public void setEtc(String etc) {
		this.etc = etc;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOrderb() {
		return orderb;
	}
	public void setOrderb(String orderb) {
		this.orderb = orderb;
	}
	public String getAddrem() {
		return addrem;
	}
	public void setAddrem(String addrem) {
		this.addrem = addrem;
	}
	
}
